class Solution {

    /*
        Function    : sum
        Description : The function is used for the addition of the two numbers.
        @author     : Pravin Mhaske
        @param      : int num1, int num2
        @return     : int - sum of two numbers
        Date        : 29/06/2025
    */
    private int sumOfTwoNumbers(int num1, int num2) {
        return num1 + num2;
    }

    /*
        Analysis:
        Time Complexity  : O(1)
        Space Complexity : O(1)
    */
    
    public int sum(int num1, int num2) {
        return sumOfTwoNumbers(num1, num2);
    }
}